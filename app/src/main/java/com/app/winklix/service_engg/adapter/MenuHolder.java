public class MenuHolder {
   public String titleName;
   public String icons;

    public MenuHolder() {
        this.titleName = titleName;
        this.icons = icons;
    }

    public String getIcons() {
        return icons;
    }

    public void setIcons(String icons) {
        this.icons = icons;
    }

    public String getTitleName()
    {
        return titleName;
    }

    public void setTitleName(String titleName)
    {
        this.titleName = titleName;
    }


}
